package org.example.Java3Loops;

public class equalnotequal {
    public static void main(String args[]){
        int n = 10;
        int x = 3;

        if(x==n){
            System.out.println("BOTH ARE SAME");
        }
        else if(x!=n){
            System.out.println("BOTH ARE DIFFERENT");
        }
        else{
            System.out.println("DEFAULT");
        }
    }
}
